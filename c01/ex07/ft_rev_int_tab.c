void	ft_rev_int_tab(int *tab, int size)
{	
	int	swap;
	int	i;
	int	f;

	i = 0;
	f = size - 1;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab[i] = tab[f];
		tab[f] = swap;
		i++;
		f--;
	}
}
