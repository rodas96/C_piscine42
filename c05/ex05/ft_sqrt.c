int	ft_sqrt(int nb)
{
	int	i;
	int	r;

	i = 1;
	r = 0;
	if (nb == 1)
		return (1);
	while (nb > 1)
	{
		nb = nb - i;
		i = i + 2;
		r++;
	}
	if (nb == 0)
		return (r);
	return (0);
}
